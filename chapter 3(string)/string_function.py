a="hello world"
string_length=len(a)#calculating length of string
print(string_length)
string_end_with_d=a.endswith("d")#checking if string ends with 'd'
print(string_end_with_d)
string_count_l=a.count("l")#counting number of 'l' in string
print(string_count_l)
string_capitalize=a.capitalize()#capitalizing first letter of string
print(string_capitalize)
string_find_word=a.find("world")#finding index of 'world' in string
print(string_find_word)
string_replace=a.replace("hello","hi")#replacing 'hello' with 'hi'
print(string_replace)