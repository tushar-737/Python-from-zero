list=[1,7,5,9,34,78,5,85]
print(list) # prints the original list
list.sort() # sorts the list in ascending order
print(list)
list.reverse() # reverses the list
print(list)
list.append(100) # adds 100 at the end of the list
print(list)
list.insert(2,50) # inserts 50 at index 2
print(list)
list.remove(5) # removes the first occurrence of 5
print(list)
print(list.pop()) # removes and prints the last element
print(list)
list.pop(3) # removes the element at index 3
print(list)
