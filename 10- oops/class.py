class car: #class definition
    price=300000 #class variable
    milage=15

maruti=car() #creating object of class car
maruti.name="maruti suzuki" #instance variable
print(maruti.name)
print(maruti.price)
print(maruti.milage)

honda=car()
honda.name="honda city" #instance variable
print(honda.name) #printing instance variable
print(honda.price) #printing class variable
print(honda.milage)